package exercicios;

public class Exercicio01 {

    public static void main(String[] args) {
        double resultado;

        resultado = 15 + 7;
        System.out.println(resultado);

        resultado = 15 - 7;
        System.out.println(resultado);

        resultado = 15 * 7;
        System.out.println(resultado);

        resultado = 15 / 7;
        System.out.println(resultado); // int / int ==> resultado será int

        resultado = 15.0 / 7;
        System.out.println(resultado); // double / int ==> resultado será double
    } 
}